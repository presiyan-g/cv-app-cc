export function SectionItem({ section, isActive, onClick }: SectionItemProps) {
  const { updateSection } = useCVStore();
  const {
    attributes,
    listeners,
    setNodeRef,
    transform,
    transition,
    isDragging,
  } = useSortable({ id: section.id });

  const style = {
    transform: CSS.Transform.toString(transform),
    transition,
  };

  const handleToggle = (e: React.MouseEvent) => {
    e.stopPropagation();
    updateSection(section.id, { enabled: !section.enabled });
  };

  return (
    <div
      ref={setNodeRef}
      style={style}
      className={cn(
        'flex items-center gap-2 px-3 py-2 rounded-lg mb-1 transition-colors',
        isDragging && 'opacity-50',
        isActive
          ? 'bg-primary-50 text-primary-700 border border-primary-200'
          : 'hover:bg-gray-50 text-gray-700',
        !section.enabled && 'opacity-50'
      )}
    >
      {/* Drag Handle */}
      <button
        {...attributes}
        {...listeners}
        className="cursor-grab active:cursor-grabbing text-gray-400 hover:text-gray-600"
      >
        <svg className="w-4 h-4" fill="currentColor" viewBox="0 0 20 20">
          <path d="M7 2a2 2 0 1 0 .001 4.001A2 2 0 0 0 7 2zm0 6a2 2 0 1 0 .001 4.001A2 2 0 0 0 7 8zm0 6a2 2 0 1 0 .001 4.001A2 2 0 0 0 7 14zm6-8a2 2 0 1 0-.001-4.001A2 2 0 0 0 13 6zm0 2a2 2 0 1 0 .001 4.001A2 2 0 0 0 13 8zm0 6a2 2 0 1 0 .001 4.001A2 2 0 0 0 13 14z" />
        </svg>
      </button>

      {/* Section Content */}
      <button
        onClick={onClick}
        className="flex-1 flex items-center gap-2 text-left"
      >
        {sectionIcons[section.type]}
        <span className="text-sm font-medium">{section.title}</span>
      </button>

      {/* Enable/Disable Toggle */}
      <div onClick={handleToggle}>
        <Switch
          checked={section.enabled}
          onChange={() => updateSection(section.id, { enabled: !section.enabled })}
        />
      </div>
    </div>
  );
}
